    private void bModificarRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarRutaActionPerformed

        int filaSeleccionada = tablaRutas.getSelectedRow();                
        
        if (filaSeleccionada != -1) {
            
            filaSeleccionada = tablaRutas.getRowSorter().convertRowIndexToModel(filaSeleccionada);

            DefaultTableModel modelo = (DefaultTableModel) tablaRutas.getModel();

            String codigo = modelo.getValueAt(filaSeleccionada, 0).toString();
            String nombre = modelo.getValueAt(filaSeleccionada, 1).toString();
            String descripcion = modelo.getValueAt(filaSeleccionada, 2).toString();

            VentanaDatosRuta vDatosRuta = new VentanaDatosRuta(this, "Modificacion", new Ruta(codigo, nombre, descripcion));

            vDatosRuta.setVisible(true);
            this.setVisible(false);

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar la ruta a modificar", "Error", JOptionPane.WARNING_MESSAGE);
        }


    }//GEN-LAST:event_bModificarRutaActionPerformed

