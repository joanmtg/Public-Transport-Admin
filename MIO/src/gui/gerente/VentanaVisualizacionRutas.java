    public void llenarTablaRutas() {

        ArrayList<Ruta> rutas = controladorRuta.obtenerRutas();

        DefaultTableModel modelo = (DefaultTableModel) tablaRutas.getModel();
        modelo.setRowCount(0);

        System.out.println(rutas.size());

        if (rutas != null) {

            for (int i = 0; i < rutas.size(); i++) {
                Ruta rutaActual = rutas.get(i);

                Object fila[] = new Object[3];

                fila[0] = rutaActual.getCodigo();
                fila[1] = rutaActual.getNombre();
                fila[2] = rutaActual.getDescripcion();

                modelo.addRow(fila);

            }

        } else {
            JOptionPane.showMessageDialog(null, "No hay rutas registradas en la base de datos");
        }

    }

