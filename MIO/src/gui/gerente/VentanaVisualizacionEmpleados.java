    private void bModificarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarEmpleadoActionPerformed
        
        //Nuevo : Jhonier
        if(tablaEmpleados.getSelectedRowCount() == 1){
            
            int filaSeleccionada = tablaEmpleados.getSelectedRow();
            String identificacion = (String)tablaEmpleados.getValueAt(filaSeleccionada, 0);
                    
            Empleado empleadoModificar = contradorEmp.obtenerEmpleado(identificacion);
            VentanaDatosEmpleado ventanaRegistro = new VentanaDatosEmpleado(this, "Modificacion", empleadoModificar);
            ventanaRegistro.setVisible(true);
            this.setVisible(false);  
            
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una fila", "Warning", JOptionPane.WARNING_MESSAGE);
        } 
        
    }//GEN-LAST:event_bModificarEmpleadoActionPerformed

