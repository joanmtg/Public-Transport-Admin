    public void insertarInfoEmpleados(){
        
        if(usuarioActual.getCargo().equals("Gerente")){
            contradorEmp.informacionEmpleados(tablaEmpleados, "");
        }
        else{//el usuari loggeado es un director de estación
            //Se llena la tabla sólo con auxiliares
            String condicion = "WHERE cargo = 'Auxiliar'";
            contradorEmp.informacionEmpleados(tablaEmpleados, condicion);            
        }
    }
    
    
