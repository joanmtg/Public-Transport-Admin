    private void bTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTerminarActionPerformed

        // Nuevo : Jhonier
        String identificacion = tfidentificacion.getText();
        String nombre = tfNombre.getText();
        String direccion = tfDir.getText();
        String telefono = tfTel.getText();

        if (identificacion.equals("") || nombre.equals("") || direccion.equals("") || telefono.equals("")
                || cbCargo.getSelectedIndex() == 0 || cbEstado.getSelectedIndex() == 0) {

            JOptionPane.showMessageDialog(null, "Debe ingresar toda la información requerida", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else {
            int dia = Integer.parseInt(cbDia.getSelectedItem().toString());
            int mes = Integer.parseInt(cbMes.getSelectedItem().toString());
            int año = Integer.parseInt(cbAño.getSelectedItem().toString());

            String fechaNacimiento = dia + "-" + mes + "-" + año;
            String cargo = (String) cbCargo.getSelectedItem();
            String estado = (String) cbEstado.getSelectedItem();
            double salario = Double.parseDouble(tfSalario.getText());
            String password = identificacion + "-" + año;

            if (operacion.equals("Registro")) {
                controladorEmp.registrarEmpleado(identificacion, nombre, direccion, telefono, fechaNacimiento,
                        cargo, estado, salario, password);
            } else if (operacion.equals("Modificacion")) {
                controladorEmp.modificarEmpleado(identificacion, nombre, direccion, telefono, fechaNacimiento,
                        cargo, estado, salario, password);
            }
            limpiarCampos();
        }


    }//GEN-LAST:event_bTerminarActionPerformed

