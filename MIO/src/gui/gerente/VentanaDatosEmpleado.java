    public VentanaDatosEmpleado(JFrame anterior, String operacion, Empleado empleado) {
        super("Registro/Modificación de empleado");
        initComponents();

        ventanaAnterior = anterior;
        this.operacion = operacion;
        empleadoModificar = empleado;
        
        setLocationRelativeTo(null);

        Calendar date = Calendar.getInstance();

        int dia, mes, año;
        dia = date.get(Calendar.DATE);
        mes = date.get(Calendar.MONTH) + 1;
        año = date.get(Calendar.YEAR);

        labelFecha.setText(dia + "/" + mes + "/" + año);

        if (operacion.equals("Modificacion")) {
            tfidentificacion.setEnabled(false);
            bLimpiar.setEnabled(false);
            //poner los datos actuales del empleado en el campo

            tfidentificacion.setText(empleadoModificar.getId());
            tfNombre.setText(empleadoModificar.getNombre());
            tfTel.setText(empleadoModificar.getTelefono());
            tfDir.setText(empleadoModificar.getDireccion());

            String[] fecha = empleadoModificar.getFechaNacimiento().split("-");

            cbDia.setSelectedItem(fecha[0]);
            cbMes.setSelectedItem(fecha[1]);
            cbAño.setSelectedItem(fecha[2]);

            cbCargo.setSelectedItem(empleadoModificar.getCargo());
            cbEstado.setSelectedItem(empleado.getEstado());
            cbEstado.setEnabled(true);
        } 
        

    }

