    private void cbCargoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCargoActionPerformed

        // Nuevo : Jhonier
        String cargo = (String) cbCargo.getSelectedItem();
        float salGerente = 3000000;
        float salDirOp = 2000000;
        float salDirEst = 1500000;
        float salCond = 800000;
        float salAux = 900000;

        if (cargo.equalsIgnoreCase("Gerente")) {
            tfSalario.setText("" + salGerente);
        } else if (cargo.equalsIgnoreCase("Director Operativo")) {
            tfSalario.setText("" + salDirOp);
        } else if (cargo.equalsIgnoreCase("Director de Estacion")) {
            tfSalario.setText("" + salDirEst);
        } else if (cargo.equalsIgnoreCase("Auxiliar")) {
            tfSalario.setText("" + salAux);
        } else if (cargo.equalsIgnoreCase("Conductor")) {
            tfSalario.setText("" + salCond);
        } else {
            tfSalario.setText("");
        }
    }//GEN-LAST:event_cbCargoActionPerformed

