    private void bTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTerminarActionPerformed

        String placa1 = tfPlaca1.getText();
        String placa2 = tfPlaca2.getText();                       
        
        String placa = placa1+"-"+placa2;
        String tipo = cbTipo.getSelectedItem().toString();
        int capacidad = Integer.parseInt(spCapacidad.getValue().toString());
        String codRuta = cbRuta.getSelectedItem().toString();
        
        if((cbRuta.getSelectedIndex() == 0) || (cbTipo.getSelectedIndex() == 0) || (placa.length() != 7)){
        
            JOptionPane.showMessageDialog(null, "Información ingresada incompleta o incorrecta", "Error", JOptionPane.WARNING_MESSAGE);
                   
        }
        else{              
            
            //Si el bus ya existe y se va a registrar
            if(operacion.equals("Registro") && controladorBus.existeBus(placa)){
                JOptionPane.showMessageDialog(null, "Ya esiste un bus con la placa " + placa + " registrado", "Error", JOptionPane.WARNING_MESSAGE);
            }
            
            else{
                
                Bus newBus = new Bus(placa, capacidad, tipo, codRuta);
                int resultado = controladorBus.guardarModificarBus(newBus, operacion);
                
                if(resultado != -1){
                
                if(operacion.equals("Registro")){
                    JOptionPane.showMessageDialog(null, "Bus guardado correctamenete");
                    reiniciarCampos();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Información del bus modificada correctamente");
                    this.dispose();
                    ventanaAnterior.setVisible(true);
                    ventanaAnterior.setLocationRelativeTo(null);
            
                    VentanaVisualizarBuses vBuses = (VentanaVisualizarBuses) ventanaAnterior;
                    vBuses.llenarTablaBuses("Todos");
                }     
                
            }   
                
            } 
            
        }
        
        
        
    }//GEN-LAST:event_bTerminarActionPerformed

