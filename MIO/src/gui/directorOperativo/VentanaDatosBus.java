    public void reiniciarCampos(){
        
        tfPlaca1.setText("");
        tfPlaca2.setText("");
        
        cbTipo.setSelectedIndex(0);
        spCapacidad.setValue(50);
        
        controladorRuta.obtenerCodRutas(cbRuta);
        cbRuta.setSelectedIndex(0);
        
        
    }
    
    public void llenarDatosModificar(){
        
        tfPlaca1.setEnabled(false);
        tfPlaca2.setEnabled(false);
        
        String placa = busModificar.getPlaca();           
        cbTipo.setSelectedItem(busModificar.getTipo());
        spCapacidad.setValue(busModificar.getCapacidad());
        cbRuta.setSelectedItem(busModificar.getCod_ruta());
        
        String[] placas = placa.split("-");
        
        tfPlaca1.setText(placas[0]);
        tfPlaca2.setText(placas[1]);        
        
    }

