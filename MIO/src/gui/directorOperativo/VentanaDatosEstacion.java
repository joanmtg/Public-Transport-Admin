    private void bTerminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bTerminarActionPerformed
        
        if(cbDirector.getSelectedIndex() == 0){
            
            JOptionPane.showMessageDialog(null, "Debe seleccionar un director para la estación", "Error", JOptionPane.WARNING_MESSAGE);            
        }
        else {                                    
            
            String nombre = tfNombre.getText();
            String ubicacion = tfUbicacion.getText();
            String telefono = tfTel.getText();
            String codDirector = cbDirector.getSelectedItem().toString();
            
            if(nombre.equals("") || ubicacion.equals("") || telefono.equals("")){
                JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Error", JOptionPane.WARNING_MESSAGE);
            }
            
            else{
                
                int resultado = -1;
                
                if(operacion.equals("Registro")){
                    
                    Estacion estacion = new Estacion(0, nombre, ubicacion, telefono, codDirector);
                    resultado = controladorEstacion.guardarModificarEstacion(estacion, operacion);
                    
                    if(resultado != -1){
                        
                        JOptionPane.showMessageDialog(null, "La estación fue guardada correctamente");                        
                        reiniciarInformacion();
                    }
                    
                }
                else{//operacion = Modificacion
                                     
                    estacionModificar = new Estacion(estacionModificar.getCodigo(), nombre, ubicacion, telefono, codDirector);
                    
                    resultado = controladorEstacion.guardarModificarEstacion(estacionModificar, operacion);
                    
                    if(resultado != -1){
                        JOptionPane.showMessageDialog(null, "La información de la estación fue modificada correctamente");
                        this.dispose();
                        ventanaAnterior.setVisible(true);
                        ventanaAnterior.setLocationRelativeTo(null);
                        VentanaVisualizarEstaciones vAnterior = (VentanaVisualizarEstaciones) ventanaAnterior;
                        vAnterior.llenarTablaEstaciones();
                        
                    }
                    
                }                
                
                
                
            }      
            
        }
        
        
        
                
    }//GEN-LAST:event_bTerminarActionPerformed

