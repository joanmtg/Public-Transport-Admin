    private void cbDirectorItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbDirectorItemStateChanged
        
        if(cbDirector.getSelectedIndex() != 0){
            
            if(cbDirector.getSelectedItem() != null){
                
                String codDirector = cbDirector.getSelectedItem().toString();

                String cod, nombre;

                for (int i = 0; i < directoresDisponibles.size(); i++) {

                    cod = directoresDisponibles.get(i).getId();
                    nombre = directoresDisponibles.get(i).getNombre();

                    if(codDirector.equals(cod)){
                        lNombreDir.setText("Nombre: " + nombre);
                        break;
                    }                   

                }
                
            }           
            
        }
        
    }//GEN-LAST:event_cbDirectorItemStateChanged

