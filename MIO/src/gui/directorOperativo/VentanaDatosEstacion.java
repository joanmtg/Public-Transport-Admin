    public VentanaDatosEstacion(JFrame anterior, String operacion, Estacion estacion) {
        
        super("Registro/Modificacion de Estación");
        initComponents();
        
        ventanaAnterior = anterior;
        this.operacion = operacion;
        estacionModificar = estacion;
                
        setLocationRelativeTo(null);
                     
        Calendar fecha = Calendar.getInstance();
        
        int dia, mes, año;
        dia = fecha.get(Calendar.DATE);
        mes = fecha.get(Calendar.MONTH) + 1;
        año = fecha.get(Calendar.YEAR);
        
        labelFecha.setText(dia + "/" + mes + "/" + año);
                
        
        reiniciarInformacion();
                
        if(operacion.equals("Modificacion")){
            
            llenarDatosModificar();            
        } 
        
          
                
    }
    
    public void reiniciarInformacion(){
        
        cbDirector.removeAllItems();
        cbDirector.addItem("Seleccione un director");
        
        tfNombre.setText("");
        tfUbicacion.setText("");
        tfTel.setText("");
        
        lNombreDir.setText("Nombre: ");
        
        directoresDisponibles = controladorEmpleado.obtenerDirectoresEstacionDisponibles();          
        
        for (int i = 0; i < directoresDisponibles.size(); i++) {
            Empleado director = directoresDisponibles.get(i);
            cbDirector.addItem(director.getId());
        }  
        
    }
    
    
    public void llenarDatosModificar(){
        
        //Informacion de la estacion a modificar
        
        tfNombre.setText(estacionModificar.getNombre());
        tfUbicacion.setText(estacionModificar.getUbicacion());
        tfTel.setText(estacionModificar.getTelefono());
        lNombreDir.setText("Nombre: " + estacionModificar.getDirector());
        
        //Se extrae el codigo del director de la estacion
        
        String codDirector = controladorEstacion.directorEstacion(estacionModificar.getCodigo());
           
        //Se añade al ComboBox de codigos de directores y se pone como seleccionado
        
        cbDirector.addItem(codDirector);
        cbDirector.setSelectedItem(codDirector);
        
        //Se añade el director al AarrayList de empleados seleccionados
        
        directoresDisponibles.add(new Empleado(codDirector, estacionModificar.getDirector(), "", "", "", 0, "", "", ""));
                        
        //Se pone el codido del director de la estacion en la estacion a modificar (antes estaba el nombre) 
                
        estacionModificar.setDirector(codDirector);
                
        
    }


