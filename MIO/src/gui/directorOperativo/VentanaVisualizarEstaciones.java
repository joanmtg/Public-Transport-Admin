    private void bModificarEstaciónActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarEstaciónActionPerformed
        
        int filaSeleccionada = tablaEstaciones.getSelectedRow();
        DefaultTableModel modelo = (DefaultTableModel) tablaEstaciones.getModel();               
        
        if(filaSeleccionada != -1){
            
            filaSeleccionada = tablaEstaciones.getRowSorter().convertRowIndexToModel(filaSeleccionada);
            
            int codigo = Integer.parseInt(modelo.getValueAt(filaSeleccionada, 0).toString());
            String nombre = modelo.getValueAt(filaSeleccionada, 1).toString();
            String ubicacion = modelo.getValueAt(filaSeleccionada, 2).toString();
            String telefono = modelo.getValueAt(filaSeleccionada, 3).toString();
            String director = modelo.getValueAt(filaSeleccionada, 4).toString();
            
            Estacion estacion = new Estacion(codigo, nombre, ubicacion, telefono, director);
            
            VentanaDatosEstacion vEstacion = new VentanaDatosEstacion(this, "Modificacion", estacion);
            vEstacion.setVisible(true);
            this.setVisible(false);
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar una estación para modificarla", "Error", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_bModificarEstaciónActionPerformed

