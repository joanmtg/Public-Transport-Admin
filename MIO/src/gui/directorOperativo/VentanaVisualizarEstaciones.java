    public void llenarTablaEstaciones(){
        
        ArrayList<Estacion> estaciones = controladorEstacion.listaEstaciones();
        
        DefaultTableModel modelo = (DefaultTableModel)tablaEstaciones.getModel();
        modelo.setRowCount(0);                
        
        if(estaciones != null){
            
            for (int i = 0; i < estaciones.size(); i++) {
                Estacion estacionActual = estaciones.get(i);
                
                Object fila[] = new Object[5];
                
                fila[0]= estacionActual.getCodigo();
                fila[1]= estacionActual.getNombre();
                fila[2]= estacionActual.getUbicacion();
                fila[3]= estacionActual.getTelefono();
                fila[4]= estacionActual.getDirector();
                                
                modelo.addRow(fila);
                
            }  
            
        }
        else{
            JOptionPane.showMessageDialog(null, "No hay estaciones registradas en la base de datos");
        }     
        
    }  

