    private void bSeleccionarEstacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSeleccionarEstacionActionPerformed
        
        int[] filasSeleccionadas = tablaEstaciones.getSelectedRows();
        DefaultTableModel modelo = (DefaultTableModel) tablaEstaciones.getModel();
               
        if(filasSeleccionadas.length != 0) {
            
            ArrayList<Estacion> estaciones = new ArrayList<>();
                        
            for (int i = 0; i < filasSeleccionadas.length; i++) {
                
                filasSeleccionadas[i] = tablaEstaciones.getRowSorter().convertRowIndexToModel(filasSeleccionadas[i]);
                
                int codigo = Integer.parseInt(modelo.getValueAt(filasSeleccionadas[i], 0).toString());
                String nombre = modelo.getValueAt(filasSeleccionadas[i], 1).toString();                               
                
                estaciones.add(new Estacion(codigo, nombre, "", "", ""));                
            }            
            
            VentanaDatosRuta vRuta = (VentanaDatosRuta) ventanaAnterior;
            vRuta.añadirEstaciones(estaciones);
            
            this.dispose();
            vRuta.setVisible(true);
            vRuta.setLocationRelativeTo(null);
            
        }        
        
        else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar una estación para continuar", "Error", JOptionPane.WARNING_MESSAGE);
        }
        
        
        
        
        
    }//GEN-LAST:event_bSeleccionarEstacionActionPerformed

