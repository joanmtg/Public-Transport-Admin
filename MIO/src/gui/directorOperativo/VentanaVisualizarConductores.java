    private void bModificarConductorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bModificarConductorActionPerformed
       
        int filaSeleccionada = tablaConductores.getSelectedRow(); 
        
        if(filaSeleccionada != -1){
            
            filaSeleccionada = tablaConductores.getRowSorter().convertRowIndexToModel(filaSeleccionada);
            
            DefaultTableModel modelo = (DefaultTableModel) tablaConductores.getModel();
                                    
            String codConductor = modelo.getValueAt(filaSeleccionada, 0).toString();
            String nombreConductor = modelo.getValueAt(filaSeleccionada, 1).toString();
            String placaConductor = modelo.getValueAt(filaSeleccionada, 2).toString();
                        
            VentanaDatosConductor ventanaRegistro = new VentanaDatosConductor(this, codConductor, nombreConductor, placaConductor);
            ventanaRegistro.setVisible(true);
            this.setVisible(false);        
        }
        else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar un conductor para modificar", "Error", JOptionPane.WARNING_MESSAGE);
        }
        
        

    }//GEN-LAST:event_bModificarConductorActionPerformed

