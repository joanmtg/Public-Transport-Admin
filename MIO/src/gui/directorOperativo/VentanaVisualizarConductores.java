    public void llenarTablaConductores(){
        
        ArrayList<Empleado> conductoresAsignados = controladorEmpleado.obtenerConductores("Asignados");
        ArrayList<Empleado> conductoresSinAsignar = controladorEmpleado.obtenerConductores("Sin Asignar");
        
        DefaultTableModel modelo = (DefaultTableModel) tablaConductores.getModel();
        modelo.setNumRows(0);        
        
        for (int i = 0; i < conductoresAsignados.size(); i++) {
            Empleado conductor = conductoresAsignados.get(i);
            
            Object[] fila = new Object[3];
            
            fila[0] = conductor.getId();
            fila[1] = conductor.getNombre();
            fila[2] = conductor.getPlacaBus();
            
            modelo.addRow(fila);            
            
        }
        
        for (int i = 0; i < conductoresSinAsignar.size(); i++) {
            Empleado conductor = conductoresSinAsignar.get(i);
            
            Object[] fila = new Object[3];
            
            fila[0] = conductor.getId();
            fila[1] = conductor.getNombre();
            fila[2] = "";
            
            modelo.addRow(fila);  
            
        }
        
    }

