    private void bValidarIDPasajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bValidarIDPasajeroActionPerformed
        
        //Si el ID es correcto
        String idPasajero;
        idPasajero = tfIDCliente.getText();        
        boolean existePasajero;
        
        if(idPasajero.equals("")){
            
            JOptionPane.showMessageDialog(null, "Ingrese la identificación del pasajero", "Aviso", JOptionPane.WARNING_MESSAGE);            
        }else{
            
            existePasajero = pasajero.verificarPasajero(idPasajero);
                
            if(existePasajero == true){
                
                tfIDCliente.setEditable(false);
                cbMotivo.setEnabled(true);
                areaDescripcion.setEnabled(true);
                bRegistrarCaso.setEnabled(true);
                areaDescripcion.setEditable(true);
                bValidarIDPasajero.setEnabled(false);
                
            }else{
                
                JOptionPane.showMessageDialog(null, "El pasajero no existe", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        }    
        
    }//GEN-LAST:event_bValidarIDPasajeroActionPerformed

    private void bRegistrarCasoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegistrarCasoActionPerformed
        
        /*Se regitra el caso en la BD con todos los datos necesarios
        
        cod_caso  (se genera automático), pero toca traerlo para el número de ticket
	descripcion 
	motivo 
	fecha      (la del sistema)
	estado     (Iniciado)
        cod_estacion   (Se toma de la variable)
	id_pasajero    (Se toma del Textfield)
	id_empleado    (Se toma del usuarioActual)
        
        */
        String idPasajero, motivo, descripcion, fecha, idEmpleado; 
        int codigoEstacion;
        idPasajero = tfIDCliente.getText();
        motivo = (String) cbMotivo.getSelectedItem();
        descripcion = areaDescripcion.getText();
        fecha = labelFecha.getText();
        codigoEstacion = Integer.parseInt(codEstacion);
        idEmpleado = usuarioActual.getId();
        
        
        if(motivo.equals("Seleccione")){
            
            JOptionPane.showMessageDialog(null, "Seleccione un motivo", "Aviso", JOptionPane.WARNING_MESSAGE);
            
        }else{
            
            if(descripcion.equals("")){
                
                JOptionPane.showMessageDialog(null, "Ingrese una descripcion", "Aviso", JOptionPane.WARNING_MESSAGE);                
                
            }else{
                
                caso.registrarCaso(descripcion, motivo, fecha, codigoEstacion, idPasajero, idEmpleado);
                bValidarIDPasajero.setEnabled(true);                
                tfIDCliente.setText("");
                tfIDCliente.setEditable(true);
                cbMotivo.setEnabled(false);
                cbMotivo.setSelectedIndex(0);
                areaDescripcion.setText("");
                bRegistrarCaso.setEnabled(false);
                areaDescripcion.setEditable(true);           
                
            }          
            
            
        }
        
        
        
    }//GEN-LAST:event_bRegistrarCasoActionPerformed

