    private void bCerrarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCerrarSesionActionPerformed

        int cerrar = JOptionPane.showConfirmDialog(null,"¿Está seguro de que quiere cerrar sesión?", "Confirmación de cierre de sesión", JOptionPane.YES_NO_OPTION);

        if(cerrar == JOptionPane.YES_OPTION){
            this.dispose();
            ventanaAnterior.setVisible(true);
            ventanaAnterior.setLocationRelativeTo(null);
            
            VentanaLogin ventana = (VentanaLogin) ventanaAnterior;
            ventana.limpiarCampos();
        }

    }//GEN-LAST:event_bCerrarSesionActionPerformed

    private void bVenderTarjetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bVenderTarjetaActionPerformed
        
        //Mostrar en un JOptionPane que se va registrar la tarjeta con PIN tal a la fecha tal con un pasaje
        int nuevoPin = tarjeta.obtenerMaxPin() + 1;
        String fecha = labelFecha.getText();
        int codigoEstacion = Integer.parseInt(codEstacion);
        
        int registrar = JOptionPane.showConfirmDialog(null,"Se registrará con la siguiente información: " + "\n PIN: " + nuevoPin + 
                                                    "\n Fecha: " + fecha + "\n Número de pasajes: 1" + "\n ¿Desea registrar la tarjeta?", 
                                                    "Confirmación de registro de tarjeta", JOptionPane.YES_NO_OPTION);
        if(registrar == JOptionPane.YES_OPTION){
            
            tarjeta.registrarTarjeta(codigoEstacion, fecha);
        }
        
        
    }//GEN-LAST:event_bVenderTarjetaActionPerformed

