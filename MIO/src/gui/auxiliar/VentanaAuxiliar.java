    private void bConfirmarEstacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConfirmarEstacionActionPerformed
        
        //Cuando se seleccione una estación válida del comboBox, se activan los demás botones
        String comboEstacion;
        comboEstacion = (String) cbEstacionTrabajo.getSelectedItem();
        if(!comboEstacion.equals("Seleccione")){
            bVenderTarjeta.setEnabled(true);
            bRecargarTarjeta.setEnabled(true);
            bModificarTarjetas.setEnabled(true);
            bRegistrarCasos.setEnabled(true);
            
            cbEstacionTrabajo.setEnabled(false);
            bConfirmarEstacion.setEnabled(false);
        }else{
            
            JOptionPane.showMessageDialog(null, "Seleccione una estación válida", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        
        //Se asigna el código de la estación seleccionada a una variable del objeto        
        codEstacion = cbEstacionTrabajo.getSelectedIndex() + "";
       
        
    }//GEN-LAST:event_bConfirmarEstacionActionPerformed

