    private void bVerGraficoRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bVerGraficoRutaActionPerformed
        
        if (tablaRutas.getSelectedRowCount() == 1) {

            int filaSeleccionada = tablaRutas.getSelectedRow();             
            
            String codRuta = (String) tablaRutas.getValueAt(filaSeleccionada, 0);
            
            try {
                File path = new File("src/pdf/rutas/"+codRuta+".pdf");
                Desktop.getDesktop().open(path);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Error al abrir el archivo", "Error", JOptionPane.ERROR_MESSAGE);
            } catch(IllegalArgumentException ex){
                JOptionPane.showMessageDialog(null, "No fue posible localizar el archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_bVerGraficoRutaActionPerformed

