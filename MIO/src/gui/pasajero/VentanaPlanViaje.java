    private void blimpiarBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blimpiarBusquedaActionPerformed
        // Se limpia la información de la tabla
        DefaultTableModel modelo=(DefaultTableModel) tablaRutas.getModel();
        for(int i=0; i<tablaRutas.getRowCount(); i++){
            modelo.removeRow(i);
            i-=1;
        }
        cbEstDestino.setSelectedIndex(0);
        cbEstOrigen.setSelectedIndex(0);
        
        
    }//GEN-LAST:event_blimpiarBusquedaActionPerformed

    private void bConsultarRutasEstacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConsultarRutasEstacionActionPerformed
        String estacion = cbEstOrigen.getSelectedItem().toString();
        
        if(estacion.equals("Seleccione")){
            JOptionPane.showMessageDialog(null, "Seleccione una estación ", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else{
            controladorRuta.verRutasXEstacion(estacion, tablaRutas);
        }
        
    }//GEN-LAST:event_bConsultarRutasEstacionActionPerformed

    private void bConsultarRutasPosiblesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bConsultarRutasPosiblesActionPerformed
        // Se obtienen las estaciones seleccionadas
        String estOrigen = cbEstOrigen.getSelectedItem().toString();
        String estDestino = cbEstDestino.getSelectedItem().toString();
        
        if(estOrigen.equals("Seleccione") || estDestino.equals("Seleccione")){
            JOptionPane.showMessageDialog(null, "Seleccione las estaciones origen y destino", "Error", JOptionPane.WARNING_MESSAGE);
        }
        else{
            controladorRuta.planearViaje(estOrigen, estDestino, tablaRutas);
        }
        
    }//GEN-LAST:event_bConsultarRutasPosiblesActionPerformed

